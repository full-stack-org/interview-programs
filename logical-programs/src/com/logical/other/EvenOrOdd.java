package com.logical.other;

public class EvenOrOdd {

	public static void main(String[] args) {
		int num =10;
		
		if(num%2 == 0) {
			System.out.println("Even"); //Even
		} else{
			System.out.println("Odd");
		}
		
		num =11;
		
		if(num%2 == 0) {
			System.out.println("Even"); 
		} else{
			System.out.println("Odd");//Odd
		}
	}

}
